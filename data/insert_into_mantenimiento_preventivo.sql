-- Disparador para cambio de tipo de dato
CREATE VIEW vista_vida_util AS
SELECT * FROM vida_util_componentes;

CREATE FUNCTION insertar_vista_vida_util() RETURNS trigger AS $$
BEGIN
    INSERT INTO vida_util_componentes (
        id_componente,
        componente,
        vida_util_estimada_anios,
        id_proveedor
    ) VALUES (
        NEW.id_componente,
        NEW.componente,
        NEW.vida_util_estimada_anios::INTEGER,
        NEW.id_proveedor
    );
    RETURN NULL;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER trigger_insertar_vista
INSTEAD OF INSERT ON vista_vida_util
FOR EACH ROW EXECUTE FUNCTION insertar_vista_vida_util();

